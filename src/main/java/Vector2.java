public class Vector2 {
    public float X;
    public float Y;

    public Vector2() {
        X = 0;
        Y = 0;
    }

    public Vector2(float val) {
        X = Y = val;
    }

    public Vector2(float x, float y) {
        X = x;
        Y = y;
    }
}
